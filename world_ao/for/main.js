const characters = ['ミッキー', 'ミニー', 'ドナルド'];

// 単数系　＋複数形
for(character of characters){
  console.log(character);
}

// 'ミッキー'
// 'ミニー'
// 'ドナルド'

// for(初期値; 繰り返す条件; 増減 ++ --)
for(i = 0; i < 10; i++) {
  console.log(i);
}

for(i = 0; i < 10; i++) {
  console.log(i);
}

// 0から9まで処理が実行される
// iの初期値は0、iは10より小さい数字まで処理が実行される